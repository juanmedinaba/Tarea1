    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Empleado empleado1 = new Empleado();
        
        
        empleado1.horasT = Double.parseDouble(txtHorasT.getText());
        empleado1.valorH = Double.parseDouble(txtValorH.getText());
        empleado1.retencion = Double.parseDouble(txtPorcentajer.getText());
       
        empleado1.salarioBruto();
        empleado1.salarioNeto();
        
        txtSalariob.setText(String.valueOf((int)empleado1.salarioB));
        txtSalarion.setText(String.valueOf((int)empleado1.salarioN));

    }//GEN-LAST:event_btnCalcularActionPerformed

